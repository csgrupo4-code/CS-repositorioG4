DROP TABLE IF EXISTS ENVIO;
DROP TABLE IF EXISTS DETALLE_PEDIDO;
DROP TABLE IF EXISTS PEDIDO;
DROP TABLE IF EXISTS PRODUCTO;
DROP TABLE IF EXISTS USUARIO_CLIENTE;
DROP TABLE IF EXISTS CATEGORIA;

CREATE TABLE CATEGORIA(
    ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACTIVO BOOLEAN NOT NULL,
    NOMBRE VARCHAR(255) NOT NULL
);

CREATE TABLE USUARIO_CLIENTE(
    ID_USUARIO INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DIRECCION VARCHAR(255),
    DNI VARCHAR(8) NOT NULL,
    ESTADO_USUARIO VARCHAR(20) NOT NULL,
    NOMBRE VARCHAR(255) NOT NULL,
    PASSWORD VARCHAR(255) NOT NULL,
    TELEFONO VARCHAR(9) NOT NULL,
    USUARIO VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE PRODUCTO(
    ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACTIVO BOOLEAN NOT NULL,
    DESTACADO BOOLEAN NOT NULL,
    IMAGEN VARCHAR(1000) NOT NULL,
    NOMBRE VARCHAR(255) NOT NULL,
    PRECIO DOUBLE NOT NULL,
    CATEGORIA_ID INT NOT NULL,

    CONSTRAINT FK_PRODUCTO_CATEGORIA
        FOREIGN KEY (CATEGORIA_ID)
        REFERENCES CATEGORIA(ID)
);

CREATE TABLE PEDIDO(
    ID_PEDIDO INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DIRECCION VARCHAR(255) NOT NULL,
    DNI VARCHAR(8) NOT NULL,
    ESTADO VARCHAR(30) NOT NULL,
    FECHA VARCHAR(30) NOT NULL,
    ID_USUARIO INT NOT NULL,
    NOMBRE_CLIENTE VARCHAR(255) NOT NULL,
    TOTAL DOUBLE NOT NULL,

    CONSTRAINT FK_PEDIDO_USUARIO
        FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIO_CLIENTE(ID_USUARIO)
);

CREATE TABLE DETALLE_PEDIDO(
    ID_DETALLE INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CANTIDAD INT NOT NULL,
    ID_PEDIDO INT NOT NULL,
    ID_PRODUCTO INT NOT NULL,
    PRECIO_UNITARIO DOUBLE NOT NULL,
    SUBTOTAL DOUBLE NOT NULL,

    CONSTRAINT FK_DETALLE_PEDIDO
        FOREIGN KEY (ID_PEDIDO)
        REFERENCES PEDIDO(ID_PEDIDO),

    CONSTRAINT FK_DETALLE_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO)
        REFERENCES PRODUCTO(ID)
);

CREATE TABLE ENVIO(
    ID_ENVIO INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ESTADO VARCHAR(30) NOT NULL,
    FECHA_ENTREGA VARCHAR(30),
    ID_PEDIDO INT NOT NULL,
    OBSERVACION VARCHAR(255),

    CONSTRAINT FK_ENVIO_PEDIDO
        FOREIGN KEY (ID_PEDIDO)
        REFERENCES PEDIDO(ID_PEDIDO)
);